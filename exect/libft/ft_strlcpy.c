#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)

{
	size_t	i;
	size_t	s;

	s = 0;
	i = 0;
	while (src[s] != '\0')
	{
		s++;
	}
	if (dstsize == 0)
		return (s);
	while (src[i] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (s);
}
